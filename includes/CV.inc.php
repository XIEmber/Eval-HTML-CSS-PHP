<div id="mazikeenpowers">
  <h1>Ses pouvoirs</h1>
  <ul>
    <li>Immortalité : En tant que démon, Mazikeen ne peut pas être tué par des moyens ordinaires. Elle ne vieillit pas et a vécu pendant des millénaires ou peut-être plus longtemps. Cependant, les démons n'ont pas d'âme, donc si elle meurt, elle serait éradiquée de l'existence. </li>
    <li>Force surhumaine : Mazikeen possède une force supérieure à l'homme; elle pouvait facilement soulever un homme adulte et le pousser dans un mur. Cependant, sa force est inférieure à celle des anges. </li>
    <li>Vitesse surhumaine : Mazikeen est capable de se déplacer beaucoup plus vite qu'un être humain, elle a été capable de passer d'une chaise à River qui était de l'autre côté de la pièce instantanément. </li>
  </ul>
  <h1>Ses habilités</h1>
  <ul>
    <li>Combattant expert corps à corps : Mazikeen a montré une grande compétence de combat non armée. Pendant qu'elle était vaincue, elle était capable de combattre Uriel sur un pied d'égalité pendant un certain temps. </li>
    <li>Experte en bataille avec des bâtons : Pendant un combat dans un bar, Maze a brisé un bâton de piscine en bois en deux morceaux et les a facilement utilisés pour vaincre un groupe d'hommes saouls. </li>
    <li>Combattant de couteau expert : Mazikeen a également montré une grande habileté au combat avec des couteaux démoniaques quand elle a combattu Amenadiel, bien qu'elle fût finalement incapable de vaincre l'ange. Elle était aussi capable de tenir tête à Uriel pendant un certain temps avant d'être privée de ses couteaux. </li>
    <li>Persuasion : Dans une certaine mesure, Mazikeen peut convaincre les gens de prendre une décision qu'ils pensent être la leur. </li>
  </ul>
